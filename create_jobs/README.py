# MAGIC # Create Jobs on Databricks
# MAGIC This repository demonstrates various examples of creating and running jobs on Databricks, including how to use widgets and parameters for dynamic workflows.
# MAGIC
# MAGIC ## File Overview
# MAGIC - 00_example_lambda: Example of a simple lambda function.
# MAGIC - 01_create_jobs_on_databricks: Demonstrates how to programmatically create jobs on Databricks.
# MAGIC - 02_create_multi_task_jobs_with_job_compute_and_run: 
# MAGIC Example of creating and running multi-task jobs with custom compute settings.
# MAGIC - 03_example_lambda_with_param: Example of a job that uses a widget for dynamic parameter input.
# MAGIC - 04_run_jobs_with_param: Builds upon 03_example_lambda_with_param to run a job using a widget input from the previous notebook.
# MAGIC - plots folder: Contains plots or visualizations related to the workflows.
# MAGIC
# MAGIC ## How to Use
# MAGIC - Start with 01_create_jobs_on_databricks to learn the basics of creating jobs.
# MAGIC - Explore 02_create_multi_task_jobs_with_job_compute_and_run for advanced multi-task job setups.
# MAGIC - Use 03_example_lambda_with_param to understand widget-based parameterization.
# MAGIC - Run 04_run_jobs_with_param to see how parameterized inputs from 03_example_lambda_with_param are used in a job run.
